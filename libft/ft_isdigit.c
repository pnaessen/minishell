#include "libft.h"

int	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if (c[i] == '-' || c[i] == '+')
			i++;
		else if (!(c[i] >= '0' && c[i] <= '9'))
			return (1);
		else
			i++;
	}
	return (0);
}
